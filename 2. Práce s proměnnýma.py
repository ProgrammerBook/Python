# Vytvoření proměnných
x = 5
y = 3

# Sčítání
z = x + y
print("Sčítání:", z)  # Očekávaný výstup: 8

# Odčítání
z = x - y
print("Odčítání:", z)  # Očekávaný výstup: 2

# Násobení
z = x * y
print("Násobení:", z)  # Očekávaný výstup: 15

# Dělení
z = x / y
print("Dělení:", z)  # Očekávaný výstup: 1.6666666666666667

# Celočíselné dělení (floor division)
z = x // y
print("Celočíselné dělení:", z)  # Očekávaný výstup: 1

# Zbytek po dělení (modulo)
z = x % y
print("Zbytek po dělení:", z)  # Očekávaný výstup: 2

# Umocnění
z = x ** y
print("Umocnění:", z)  # Očekávaný výstup: 125

# Možnosti s proměnnými

#Proměnné mohou být modifikovány, překopírovány nebo mohou být použity k ukládání různých hodnot.

# Změna hodnoty proměnné
x = 5
x = 10
print("Hodnota proměnné x je:", x)  # Očekávaný výstup: 10

# Překopírování hodnoty proměnné
x = 10
y = x
print("Hodnota proměnné y je:", y)  # Očekávaný výstup: 10